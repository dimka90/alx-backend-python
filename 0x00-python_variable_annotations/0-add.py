#!/usr/bin/env python3

"""This program add the two float numbers and return sum as a float."""


def add(a:float, b:float) -> float:
    """
    Add two floats.

    Arguments:
            a: float representing the first number to be addded.
            b: float representing the second number to be added.
    Returns:
            containning the addition of two floats numbers A float.
    """
    return a + b
    
